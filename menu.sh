valid_port() {
  [[ "$1" =~ ^[0-9]+$ ]] || return 1
  (( $1 >= 1 && $1 <= 65535 )) || return 1
}

unit_name() { echo "proxy@${1}.service"; }
unit_template_path() { echo "${UNIT_DIR}/proxy@.service"; }
env_path() { echo "${ENV_DIR}/proxy${1}.env"; }

is_port_in_use() {
  local port="$1"
  if command -v ss >/dev/null 2>&1; then
    ss -lnt 2>/dev/null | awk '{print $4}' | grep -Eq "(:|\\])${port}$"
    return $?
  fi
  if command -v lsof >/dev/null 2>&1; then
    lsof -iTCP:"$port" -sTCP:LISTEN >/dev/null 2>&1
    return $?
  fi
  return 1
}

# Escreve STATUS de forma segura para EnvironmentFile.
# Formato: STATUS='...'
# - troca CR/LF por espaÃ§o
# - usa escaping de aspas simples compatÃ­vel com shell: ' -> '\'' (fecha, escapa, reabre)
write_env_file() {
  local port="$1"
  local status="$2"
  local p
  p="$(env_path "$port")"

  status="${status//$'\r'/ }"
  status="${status//$'\n'/ }"
  status="${status:0:128}"

  # escape para single-quote
  local esc
  esc="$(printf "%s" "$status" | sed "s/'/'\\\\''/g")"

  cat >"$p" <<EOF
# Generated by RustyProxy Manager
STATUS='$esc'
EOF
  chmod 600 "$p"
}

ensure_unit_template() {
  local path
  path="$(unit_template_path)"

  if [[ -f "$path" ]]; then
    return 0
  fi

  cat >"$path" <<'EOF'
[Unit]
Description=RustyProxy instance on port %i
After=network-online.target
Wants=network-online.target

[Service]
Type=simple
EnvironmentFile=/etc/rustyproxy/proxy%i.env
ExecStart=/opt/rustyproxy/proxy --port %i --status ${STATUS}
Restart=always
RestartSec=2
LimitNOFILE=1048576

# Hardening (ajuste se precisar)
NoNewPrivileges=true
PrivateTmp=true
ProtectSystem=strict
ProtectHome=true
ProtectKernelTunables=true
ProtectKernelModules=true
ProtectControlGroups=true
LockPersonality=true
RestrictSUIDSGID=true
RestrictRealtime=true
MemoryDenyWriteExecute=true

[Install]
WantedBy=multi-user.target
EOF

  systemctl daemon-reload
}

upsert_port_record() {
  local port="$1"
  local status="$2"
  if grep -qE "^${port}\|" "$PORTS_FILE"; then
    sed -i "s/^${port}|.*/${port}|${status}/" "$PORTS_FILE"
  else
    echo "${port}|${status}" >> "$PORTS_FILE"
  fi
}

remove_port_record() {
  local port="$1"
  sed -i "/^${port}\|/d" "$PORTS_FILE" || true
}

add_proxy_port() {
  local port="$1"
  local status="${2:-@RustyManager}"

  valid_port "$port" || { echo -e "${RED}Porta invÃ¡lida (1-65535).${RESET}"; return; }

  if is_port_in_use "$port"; then
    echo -e "${RED}â›”ï¸ A PORTA $port JÃ ESTÃ EM USO.${RESET}"
    return
  fi

  ensure_unit_template
  write_env_file "$port" "$status"

  systemctl enable "$(unit_name "$port")" >/dev/null
  systemctl restart "$(unit_name "$port")"

  upsert_port_record "$port" "$status"
  echo -e "${GREEN}âœ… PORTA $port ATIVADA COM SUCESSO.${RESET}"
}

del_proxy_port() {
  local port="$1"
  valid_port "$port" || { echo -e "${RED}Porta invÃ¡lida (1-65535).${RESET}"; return; }

  systemctl stop "$(unit_name "$port")" >/dev/null 2>&1 || true
  systemctl disable "$(unit_name "$port")" >/dev/null 2>&1 || true

  rm -f "$(env_path "$port")"
  remove_port_record "$port"

  echo -e "${GREEN}âœ… PORTA $port DESATIVADA/REMOVIDA COM SUCESSO.${RESET}"
}

update_proxy_status() {
  local port="$1"
  local new_status="$2"

  valid_port "$port" || { echo -e "${RED}Porta invÃ¡lida (1-65535).${RESET}"; return; }

  if [[ ! -f "$(unit_template_path)" ]]; then
    echo -e "${RED}Unit template nÃ£o encontrado. Ative uma porta primeiro.${RESET}"
    return
  fi

  write_env_file "$port" "$new_status"
  systemctl restart "$(unit_name "$port")" >/dev/null 2>&1 || {
    echo -e "${YELLOW}âš ï¸ ServiÃ§o nÃ£o estava ativo, ativando...${RESET}"
    systemctl enable "$(unit_name "$port")" >/dev/null
    systemctl restart "$(unit_name "$port")"
  }

  upsert_port_record "$port" "$new_status"
  echo -e "${YELLOW}ðŸ”ƒ STATUS DA PORTA $port ATUALIZADO PARA '${new_status}'.${RESET}"
  sleep 1
}

restart_all_proxies() {
  if [[ ! -s "$PORTS_FILE" ]]; then
    echo "NENHUMA PORTA ENCONTRADA PARA REINICIAR."
    return
  fi

  echo "ðŸ”ƒ REINICIANDO TODAS AS PORTAS DO PROXY..."
  sleep 1

  ensure_unit_template

  while IFS='|' read -r port status; do
    [[ -n "${port:-}" ]] || continue
    valid_port "$port" || continue

    # garante env file consistente com o arquivo
    write_env_file "$port" "${status:-@RustyManager}"

    systemctl restart "$(unit_name "$port")" >/dev/null 2>&1 || {
      systemctl enable "$(unit_name "$port")" >/dev/null
      systemctl restart "$(unit_name "$port")"
    }
  done < "$PORTS_FILE"

  echo -e "${GREEN}âœ… TODAS AS PORTAS FORAM REINICIADAS COM SUCESSO.${RESET}"
  sleep 1
}

